    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request, Post $post)
    {
        $user = auth()->user();

        if (! $post->isLikedBy($user)) {
            $post->likes()->create(['user_id' => $user->id,]);
        }

        return back();
    }

